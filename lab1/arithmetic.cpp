#include<iostream>

using namespace std;

int main()
{
	int fn=6, sn=3;
	cout<<"Addition = "<<(fn+sn)<<endl;
	cout<<"Subtraction = "<<fn-sn<<endl;
	cout<<"Product = "<<fn*sn<<endl;
	cout<<"Quotient = "<<fn/sn<<endl;
	fn=18, sn=6;
	cout<<"Addition = "<<(fn+sn)<<endl;
	cout<<"Subtraction = "<<fn-sn<<endl;
	cout<<"Product = "<<fn*sn<<endl;
	cout<<"Quotient = "<<fn/sn<<endl;
}
